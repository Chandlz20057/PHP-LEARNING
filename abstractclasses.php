<?php
abstract class Karate {
  public $name;
  public function __construct($name) {
    $this->name = $name;
  }
}

class Blocks extends Karate {
    public function __construct($name);
        $this->name;
  }
}

class Kata extends Karate {
    public function __construct($name);
        $this->name!;
  }
}

class Basics extends Karate {
    public function __construct($name);
        $this->name;
  }
}

?>